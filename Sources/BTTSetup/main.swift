#!/bin/bash
# ─────────────────────────────────────────────────────────────────
# btt-inject.sh  —  BlueTriangle PRE-BUILD injection
# Auto-generated by BTTSetup. Do not edit manually.
# ─────────────────────────────────────────────────────────────────
set -e

ARCH=$(uname -m)
SRC_ROOT="$SRCROOT"

echo "🔍 Resolving BTTInstrumentor..."

DERIVED_DATA=$(dirname "$BUILD_DIR")/..

BUNDLE=$(find \
    "$DERIVED_DATA/SourcePackages/artifacts" \
    "$DERIVED_DATA/SourcePackages/checkouts" \
    "$SRC_ROOT/../../../" \
    -maxdepth 8 \
    -name "BTTInstrumentor.artifactbundle" \
    -type d 2>/dev/null | head -1)

if [ -z "$BUNDLE" ]; then
    echo "⚠️ BTTInstrumentor.artifactbundle not found — skipping injection"
    exit 0
fi

echo "📦 Bundle: $BUNDLE"

BINARY=$(python3 - <<EOF
import json, os, sys
bundle = "$BUNDLE"
arch = "$ARCH"
with open(os.path.join(bundle, "info.json")) as f:
    data = json.load(f)
variants = data.get("artifacts", {}).get("BTTInstrumentor", {}).get("variants", [])
for v in variants:
    if any(arch in t for t in v.get("supportedTriples", [])):
        print(os.path.join(bundle, v["path"]))
        sys.exit(0)
sys.exit(1)
EOF
)

if [ -z "$BINARY" ] || [ ! -f "$BINARY" ]; then
    echo "⚠️ Binary not found for arch: $ARCH — skipping"
    exit 0
fi

echo "⚙️ Binary: $BINARY"
chmod +x "$BINARY"

# Restore any leftover backups from a crashed previous build
find "$SRC_ROOT" -name "*.swift.bttbackup" \
    ! -path "*/Pods/*" ! -path "*/.build/*" ! -path "*/DerivedData/*" \
    | while read -r BACKUP; do
        ORIGINAL="${BACKUP%.bttbackup}"
        cp "$BACKUP" "$ORIGINAL"
        rm "$BACKUP"
        echo "♻️  Restored leftover: $(basename "$ORIGINAL")"
    done

echo "🔧 BTT Injecting (arch: $ARCH)..."

find "$SRC_ROOT" -name "*.swift" \
    ! -path "*/Pods/*" \
    ! -path "*/.build/*" \
    ! -path "*/DerivedData/*" \
    ! -path "*/BTTInstrumentor/*" \
    | while read -r FILE; do
        "$BINARY" "$FILE"
    done

echo "✅ Injection complete"
"""#

